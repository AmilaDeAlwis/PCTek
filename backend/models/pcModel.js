import mongoose from "mongoose";

const pcSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
});

const pcModel = mongoose.models.pcItem || mongoose.model("pcItem", pcSchema);

export default pcModel;
